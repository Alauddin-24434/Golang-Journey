package main

import "fmt"

func main() {
    // =============================== Hello World ===============================
    fmt.Println("Hello World")
}

// =============================== Q&A ===============================

// Q1: Go প্রোগ্রামে execution কোথা থেকে শুরু হয়?
// A1: Go প্রোগ্রামে execution সর্বদা main() ফাংশন থেকে শুরু হয়।  

// Q2: fmt.Println() কি কাজ করে?
// A2: এটি console এ text বা variable value print করে এবং line break যোগ করে।  

// Q3: Go তে program এর basic structure কীভাবে হয়?
// A3: 
// package main            -> Program package (required for executable)
// import "fmt"            -> Import standard library for input/output
// func main() {...}       -> Main function where execution starts

// Q4: Go তে ";" ব্যবহারের প্রয়োজন আছে কি?
// A4: সাধারণত না, Go তে line break ই statement শেষ নির্দেশ করে। ";" optional।
